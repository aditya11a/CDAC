public class intk
{
	
	public static void main(String[]args)
	{
		int a=7;

		System.out.println("Integer of "+a+" into Binary value:"+Integer.toBinaryString(a));
		System.out.println("Integer of "+a+" into Octal value:"+Integer.toOctalString(a));
		System.out.println("Integer of "+a+" into Hexadecimal value:"+Integer.toHexString(a));
	}
		
	
}